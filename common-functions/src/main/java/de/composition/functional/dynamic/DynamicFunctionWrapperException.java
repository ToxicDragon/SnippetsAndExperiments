package de.composition.functional.dynamic;

public class DynamicFunctionWrapperException extends RuntimeException {

	private static final long serialVersionUID = 7081713013796281222L;

	public DynamicFunctionWrapperException() {
		super();
	}

	public DynamicFunctionWrapperException(String message, Throwable cause) {
		super(message, cause);
	}

	public DynamicFunctionWrapperException(String message) {
		super(message);
	}

	public DynamicFunctionWrapperException(Throwable cause) {
		super(cause);
	}

}
